     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
		$input = $request->except(['_token']);

		$data = array(
			'user_id'  => $user_id,
			'demand_category_id'    => $input['demand_category_id'],
			'offer_category_id'  	=> $input['offer_category_id']
		);
		
		$category = new UserCategory();
		$category->saveUserCategory($data);
		
		$message = session()->flash('success', 'Kategorija je snimljena.');
		
		//return redirect()->back()->withFlashMessage($messange);
		return redirect()->route('admin.posts.index')->withFlashMessage($message);
    }

